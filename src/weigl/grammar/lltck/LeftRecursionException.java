package weigl.grammar.lltck;

public class LeftRecursionException extends Exception
{

    public LeftRecursionException()
    {
        super();

    }

    public LeftRecursionException(String message, Throwable cause)
    {
        super(message, cause);

    }

    public LeftRecursionException(String message)
    {
        super(message);

    }

    public LeftRecursionException(Throwable cause)
    {
        super(cause);

    }

}
